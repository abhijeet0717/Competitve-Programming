long long score(vector<int>&p,vector<int>&a,int s,int k){
   int n=p.size();
   long long mx=0,cur=0;
   vector<bool>vis(n);
   while(!vis[s]&&k>0){
      vis[s]=1;
      mx=max(mx,cur+1ll*k*a[s]);
      cur+=a[s];
      k--;
      s=p[s];
   }
   debug(mx);
   return mx;
}

