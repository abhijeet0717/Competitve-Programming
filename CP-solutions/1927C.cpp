void solve() 
{
  int n,m,k;cin>>n>>m>>k;
  int a[n],b[m];
  set<int> sa,sb,final;
  for (int i = 0; i < n; i++)
  {
  	cin>>a[i];
   sa.insert(a[i]);
  }
  for (int i = 0; i < m; i++)
  {
  	cin>>b[i];
   sb.insert(b[i]);
  }
  int count_1(0),count_2(0);
  for(auto it: sa){
   if(it<=k){
      count_1++;
      final.insert(it);
   }
  }
  for(auto it: sb){
   if(it<=k){
      count_2++;
      final.insert(it);
   }
  }
  if (final.size()==k && count_2>=k/2 && count_1>=k/2)
  {
     cout<<"YES\n";
  }
  else {
   cout<<"NO\n";
  }
}


